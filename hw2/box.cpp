void sample_t::init_box()
{
    glGenVertexArrays(1, &box_vao_id_);
    glBindVertexArray(box_vao_id_);

    glGenBuffers(1, &box_buf_id_);
    glBindBuffer(GL_ARRAY_BUFFER, box_buf_id_);
    glBufferData(GL_ARRAY_BUFFER, sizeof(box_vertices), box_vertices, GL_STATIC_DRAW);

    GLuint in_pos = glGetAttribLocation(box_shader_, "in_pos");
    glEnableVertexAttribArray(in_pos);
    glVertexAttribPointer(in_pos, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(GLfloat), (GLvoid*)0);

    glBindVertexArray(0);
}
